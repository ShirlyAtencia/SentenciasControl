public class ComparaNumero {
  public static void main(String[] args) {

    int numero = 0;

    if (numero > 0) {
      System.out.println("El Numero es Mayor a 0, el Numero es Positivo");
    } else if (numero < 0) {
      System.out.println("El Numero es Menor a 0, el Numero es Negativo");
    } else {
      System.out.println("El Numero es 0");
    }
  }
}
